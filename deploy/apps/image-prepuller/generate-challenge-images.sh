#!/bin/sh
# Regenerates daemonset-challenges.yaml from the CLUSTER'S OWN LabSpace/LabService
# objects — the same CRDs `llm-ctf-2026/Makefile` applies via `make crds` — so the
# pre-puller's image list always matches what's actually deployed, including the
# content-SHA workspace tags that change every time a challenge's workspace/ is
# rebuilt (see llm-ctf-2026/README.md).
#
# Run this after `make crds` whenever a challenge is added, removed, or rebuilt:
#   ./generate-challenge-images.sh > daemonset-challenges.yaml
#   git diff daemonset-challenges.yaml   # review before committing
#
# Requires: kubectl (pointed at the target cluster).
set -eu

# Only images actually hosted on the PRIVATE registry need this DaemonSet + a pull
# secret. Stock public images (explabs/ctf-school-desktop-*, the guard, joy's
# python:3.11-alpine) are covered by daemonset-platform.yaml already — including
# them here too would just duplicate that pull and misleadingly imply they need
# credentials they don't.
REGISTRY_HOST=${REGISTRY_HOST:-registry.alfa.ctf.school}

images=$(
  {
    kubectl get labservices.infra.ctf.school -o jsonpath='{range .items[*]}{.spec.image}{"\n"}{end}'
    kubectl get labspaces.infra.ctf.school -o jsonpath='{range .items[*]}{.spec.workspace.image}{"\n"}{end}'
  } | grep "^${REGISTRY_HOST}/" | sort -u
)

cat <<HEADER
# GENERATED by generate-challenge-images.sh — do not hand-edit.
#
# Per-challenge SERVICE + WORKSPACE images actually hosted on ${REGISTRY_HOST},
# re-derived from the cluster's own LabSpace/LabService objects each time a
# challenge is added/changed (see the script for exactly how). These need a pull
# secret (${REGISTRY_HOST} requires htpasswd auth even for pulls) — see
# registry-pull.secret.example.yaml and the README for how that's supplied.
#
# Same "sleep infinity, never run the real entrypoint" pattern as
# daemonset-platform.yaml — see that file for why.
apiVersion: apps/v1
kind: DaemonSet
metadata:
  name: image-prepuller-challenges
  namespace: image-prepuller
  labels: { app: image-prepuller, tier: challenges }
spec:
  selector:
    matchLabels: { app: image-prepuller, tier: challenges }
  template:
    metadata:
      labels: { app: image-prepuller, tier: challenges }
    spec:
      tolerations:
        - operator: Exists
      automountServiceAccountToken: false
      imagePullSecrets:
        - name: registry-pull
      securityContext:
        seccompProfile: { type: RuntimeDefault }
      containers:
HEADER

i=0
printf '%s\n' "$images" | while IFS= read -r image; do
  [ -z "$image" ] && continue
  i=$((i + 1))
  # Container names must be valid DNS labels — derive one from the image ref
  # (strip registry host + tag, keep it short and unique enough to read in `kubectl
  # get pods -o wide` / describe output).
  name=$(printf '%s' "$image" | sed -E 's#^[^/]+/##; s#[:@].*$##; s#_#-#g' | cut -c1-50)
  cat <<CONTAINER
        - name: "img-${i}-${name}"
          image: "${image}"
          imagePullPolicy: IfNotPresent
          command: ["sleep", "infinity"]
          resources:
            requests: { cpu: 5m, memory: 16Mi }
            limits: { cpu: 20m, memory: 32Mi }
          securityContext:
            allowPrivilegeEscalation: false
            readOnlyRootFilesystem: true
            runAsNonRoot: true
            runAsUser: 65534
            capabilities: { drop: ["ALL"] }
CONTAINER
done
